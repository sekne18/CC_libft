char	*ft_strchr(const char *s, int c)
{
	int	i;

	if (c > 127)
		return ((char *)s);
	i = -1;
	while (s[++i])
	{
		if (s[i] == c)
			return ((char *)&s[i]);
	}
	if (s[i] == c)
		return ((char *)&s[i]);
	return ((char *)0);
}
