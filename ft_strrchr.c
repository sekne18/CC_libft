char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*last;

	if (c > 127 || c < 0)
		return ((char *)s);
	last = (char *)0;
	i = 0;
	while (s[i])
	{
		if (s[i] == c)
			last = (char *)&s[i];
		i++;
	}
	if (s[i] == c)
		last = (char *)&s[i];
	return (last);
}
