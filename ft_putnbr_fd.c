#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	arr[12];
	int		i;
	int		is_neg;

	i = 0;
	is_neg = 1;
	if (n == 0)
		arr[i++] = 0 + '0';
	if (0 > n)
		is_neg = -1;
	while (n != 0)
	{
		arr[i++] = ((n % 10) * is_neg) + '0';
		n /= 10;
	}
	if (is_neg == -1)
		arr[i++] = '-';
	while (--i >= 0)
		ft_putchar_fd(arr[i], fd);
}
